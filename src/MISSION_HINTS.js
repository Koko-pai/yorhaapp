// ── Основная функция: получить подсказку для миссии ──
export function getMissionHint(mission) {
  // 1. Ищем уникальный совет по точному названию миссии
  if (mission.title && MISSION_HINTS[mission.title]) {
    return MISSION_HINTS[mission.title];
  }

  // 2. Запасной вариант: совет по категории + сложности
  const key = `${mission.category}_${mission.threat}`;
  const hints = CATEGORY_THREAT_HINTS[key];
  if (hints && hints.length > 0) {
    const idx = mission.title
      ? mission.title.split("").reduce((a, c) => a + c.charCodeAt(0), 0) % hints.length
      : Math.floor(Math.random() * hints.length);
    return hints[idx];
  }

  return "Командование подтверждает получение директивы. Выполни задачу в установленный срок. Слава человечеству.";
}
